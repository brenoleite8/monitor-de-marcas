    private static $activeRecord = 'TbMateriaisEstudo';
    private static $primaryKey = 'id';
    private static $formName = 'form_TbMateriaisEstudoList';
    private $showMethods = ['onReload', 'onSearch', 'onRefresh', 'onClearFilters'];
